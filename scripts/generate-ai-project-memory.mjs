/**
 * Concatenate project markdown docs into one AI handoff file.
 *
 * Usage:
 *   node scripts/generate-ai-project-memory.mjs
 *
 * Environment (optional):
 *   DOCS_DIR=docs
 *   OUTPUT_FILE=docs/ai/01-ai-project-memory.md
 *   PROJECT_NAME="My Project"
 *
 * Wire in package.json when adopting:
 *   "generate:ai-memory": "node scripts/generate-ai-project-memory.mjs"
 */
import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const DOCS_DIR = path.resolve(ROOT, process.env.DOCS_DIR || "docs");
const OUTPUT_FILE = process.env.OUTPUT_FILE || "docs/ai/01-ai-project-memory.md";
const OUTPUT_PATH = path.resolve(ROOT, OUTPUT_FILE);
const PROJECT_NAME = process.env.PROJECT_NAME || "<project-name>";

/** Directories to skip when walking docs (tooling, raw artefacts). */
const SKIP_DIRS = new Set(
  (process.env.SKIP_DIRS || ".obsidian,lighthouse,brand")
    .split(",")
    .map((s) => s.trim())
    .filter(Boolean),
);

/** Basenames to skip (generated output, superseded backlogs). */
const SKIP_FILES = new Set(
  [
    path.basename(OUTPUT_FILE),
    ...(process.env.SKIP_FILES || "01-ai-project-memory.md")
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean),
  ],
);

async function collectMarkdownFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (SKIP_DIRS.has(entry.name)) continue;
      files.push(...(await collectMarkdownFiles(fullPath)));
      continue;
    }
    if (!entry.name.endsWith(".md")) continue;
    if (SKIP_FILES.has(entry.name)) continue;
    files.push(fullPath);
  }

  return files;
}

function sortDocFiles(files) {
  const rel = (file) => path.relative(DOCS_DIR, file);

  const numbered = files
    .filter((file) => /^\d{2}-/.test(path.basename(file)))
    .sort((a, b) => rel(a).localeCompare(rel(b), undefined, { numeric: true }));

  const other = files
    .filter((file) => !/^\d{2}-/.test(path.basename(file)))
    .sort((a, b) => rel(a).localeCompare(rel(b), undefined, { numeric: true }));

  return [...numbered, ...other];
}

function buildPreamble(generatedAt, sourceCount) {
  const docsRel = path.relative(ROOT, DOCS_DIR).replace(/\\/g, "/") || "docs";
  return `# ${PROJECT_NAME} — AI project memory (auto-generated)

> **Do not edit this file for permanent documentation changes.**
> Edit source files under \`${docsRel}/\` instead, then regenerate with
> \`node scripts/generate-ai-project-memory.mjs\`
> or ask Cursor to "update the documentation memory file".

**Generated:** ${generatedAt}
**Source files included:** ${sourceCount} markdown documents from \`${docsRel}/\`
**Purpose:** single handoff file for ChatGPT and other assistants — full project context without uploading many files.

---

`;
}

async function main() {
  const files = sortDocFiles(await collectMarkdownFiles(DOCS_DIR));
  const generatedAt = new Date().toISOString().slice(0, 10);
  const parts = [buildPreamble(generatedAt, files.length)];
  const docsRel = path.relative(ROOT, DOCS_DIR).replace(/\\/g, "/") || "docs";

  for (const file of files) {
    const rel = path.relative(DOCS_DIR, file).replace(/\\/g, "/");
    const content = await readFile(file, "utf8");
    parts.push(
      `\n\n---\n\n<!-- SOURCE: ${docsRel}/${rel} -->\n\n# [Source: ${rel}]\n\n${content.trim()}\n`,
    );
  }

  await mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, parts.join(""), "utf8");
  console.log(`Wrote ${path.relative(ROOT, OUTPUT_PATH)} (${files.length} sources)`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
